public class Barang {
    double panjang;
    double lebar;
    double tinggi;
    double berat;

    public Barang(double panjang, double lebar, double tinggi, double berat) {
        this.panjang = panjang;
        this.lebar = lebar;
        this.tinggi = tinggi;
        this.berat = berat;
    }
}
